<div class="topic">
    Diploma of Hospitality Management in Kitchen operation - (Culinary Arts/Commercial Cooking NC II)
</div>

<div class="duration">
    Duration: 15 months Including Internship
</div>

<div class="coursedesc">
    In-depth knowledge, skills and attitude of kitchen staff in the
    art on safe food preparation. Includes training to improve
