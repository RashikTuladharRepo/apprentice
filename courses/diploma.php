    Sauces and Soups Preparation, Appetizers, Salads and Sandwiches Preparation, Poultry, Meat Cuts, and Sea
    foods Preparation and Cooking, Hot and Cold Desserts Preparation, Pastry, Cakes, and Yeast-based Products
    Preparation, Prepared Foodstuffs Packaging, Workplace Hygiene and more.
